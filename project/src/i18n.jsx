// i18n context
const I18nContext = React.createContext({ lang: "fr", t: (k) => k, setLang: () => {} });

function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => {
    try { return localStorage.getItem("cvitalis.lang") || "fr"; } catch { return "fr"; }
  });
  useEffect(() => {
    try { localStorage.setItem("cvitalis.lang", lang); } catch {}
    document.documentElement.lang = lang;
  }, [lang]);
  const t = (key, vars) => {
    const raw = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) ?? (TRANSLATIONS.fr[key] ?? key);
    if (Array.isArray(raw)) return raw;
    if (!vars) return raw;
    return String(raw).replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? "");
  };
  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

function useT() {
  return React.useContext(I18nContext);
}

Object.assign(window, { I18nProvider, useT, TRANSLATIONS });
